namespace Font3x5
{
	uint32_t Width  () { return 4; }
	uint32_t Height () { return 6; }
	
	bool UseForground(char letter, int x, int y)
	{
		uint32_t fontX  = (x         + (y % 2) * 4);
		uint32_t fontY  = letter * 3 + (y / 2)     ;
		uint8_t  symbol = data[fontY]              ;
		
		return ((symbol & (0b10000000 >> fontX)) == 0);
	}
}